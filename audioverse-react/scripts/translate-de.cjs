function main() {
  console.log('Reading locale files...');
  const en = JSON.parse(fs.readFileSync(EN_PATH, 'utf8'));
  const de = JSON.parse(fs.readFileSync(DE_PATH, 'utf8'));

  const enLeaves = getLeafPaths(en);
  const deLeaves = getLeafPaths(de);
  const deMap = new Map(deLeaves.map(l => [l.path, l.value]));

  // Find untranslated leaves (de value === en value)
  const untranslated = [];
  for (const leaf of enLeaves) {
    const deVal = deMap.get(leaf.path);
    if (deVal === leaf.value && typeof leaf.value === 'string') {
      untranslated.push(leaf);
    }
  }

  console.log(`Found ${untranslated.length} untranslated leaves.`);
  const uniqueVals = new Set(untranslated.map(u => u.value));
  console.log(`Unique untranslated values: ${uniqueVals.size}`);

  // Apply translations
  let translated = 0;
  let unchanged = 0;
  let missing = 0;
  const missingValues = new Set();

  for (const leaf of untranslated) {
    const parts = leaf.path.split('.');
    const enVal = leaf.value;

    if (dictionary.hasOwnProperty(enVal)) {
      const deVal = dictionary[enVal];
      setNestedValue(de, parts, deVal);
      if (deVal !== enVal) {
        translated++;
      } else {
        unchanged++; // Same in both languages (technical terms, game names, etc.)
      }
    } else {
      missing++;
      missingValues.add(enVal);
    }
  }

  // Write de.json back
  const output = JSON.stringify(de, null, 2) + '\n';
  fs.writeFileSync(DE_PATH, output, 'utf8');

  console.log('\n=== Translation Results ===');
  console.log(`Total untranslated leaves: ${untranslated.length}`);
  console.log(`Translated to German:      ${translated}`);
  console.log(`Unchanged (same in DE):    ${unchanged}`);
  console.log(`Missing from dictionary:   ${missing}`);

  if (missingValues.size > 0) {
    console.log(`\nMissing values (${missingValues.size}):`);
    const sorted = [...missingValues].sort();
    sorted.forEach(v => console.log(`  ${JSON.stringify(v)}`));
    // Write missing values to file for reference
    fs.writeFileSync(
      path.resolve(__dirname, 'missing-translations.json'),
      JSON.stringify(sorted, null, 2) + '\n',
      'utf8'
    );
    console.log('\nMissing values written to scripts/missing-translations.json');
  }

  console.log('\nDone! de.json has been updated.');
}

