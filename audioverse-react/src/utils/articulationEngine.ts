/**
 * Articulation Engine — per-note performance articulations.
 * Supports legato, staccato, accent, glide/portamento, vibrato, and more.
 */

export type ArticulationType =
  | 'normal'
  | 'legato'
  | 'staccato'
  | 'staccatissimo'
  | 'accent'
  | 'marcato'
  | 'tenuto'
  | 'glide'
  | 'portamento'
  | 'vibrato'
  | 'tremolo'
  | 'trill'
  | 'bend'
  | 'slide-up'
  | 'slide-down'
  | 'palm-mute'
  | 'harmonic';

export interface ArticulationConfig {
  type: ArticulationType;
  /** Intensity/depth 0-1 */
  intensity: number;
  /** Rate in Hz (for vibrato, tremolo, trill) */
  rate?: number;
  /** Duration modifier (fraction of original, 0.1-2.0) */
  durationScale?: number;
  /** Velocity modifier (added, -64 to 64) */
  velocityOffset?: number;
  /** Glide/portamento time in ms */
  glideTimeMs?: number;
  /** Pitch bend range in semitones (for bends) */
  bendSemitones?: number;
  /** Trill interval in semitones */
  trillInterval?: number;
}

export interface ArticulatedNote {
  pitch: number;
  velocity: number;
  /** Start time in seconds */
  startTime: number;
  /** Duration in seconds */
  duration: number;
  /** Applied articulation */
  articulation: ArticulationType;
  /** Pitch envelope events (time, pitch pairs relative to start) */
  pitchEnvelope?: Array<{ time: number; pitch: number }>;
  /** Velocity envelope (time, velocity pairs for dynamics) */
  velocityEnvelope?: Array<{ time: number; velocity: number }>;
  /** CC automation generated by articulation */
  ccAutomation?: Array<{ time: number; cc: number; value: number }>;
}

export interface NoteInput {
  pitch: number;
  velocity: number;
  startTime: number;
  duration: number;
}

