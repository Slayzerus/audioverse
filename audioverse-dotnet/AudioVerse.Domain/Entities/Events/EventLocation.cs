namespace AudioVerse.Domain.Entities.Events;

/// <summary>
/// Physical or virtual location for an event.
/// Supports multiple map providers: Google Maps, OpenStreetMap, Mapbox, HERE.
/// </summary>
public class EventLocation
{
    public int Id { get; set; }

    // ????????????????????????????????????????????????????????????
    //  Basic Info
    // ????????????????????????????????????????????????????????????

    /// <summary>Display name of the location.</summary>
    public string Name { get; set; } = string.Empty;
    
    /// <summary>Optional description or notes.</summary>
    public string? Description { get; set; }

    // ????????????????????????????????????????????????????????????
    //  Address Components
    // ????????????????????????????????????????????????????????????

    /// <summary>Street address (e.g., "123 Main St").</summary>
    public string? StreetAddress { get; set; }
    
    /// <summary>City name.</summary>
    public string? City { get; set; }
    
    /// <summary>State or province.</summary>
    public string? State { get; set; }
    
    /// <summary>Postal/ZIP code.</summary>
    public string? PostalCode { get; set; }
    
    /// <summary>Country name.</summary>
    public string? Country { get; set; }
    
    /// <summary>ISO 3166-1 alpha-2 country code (e.g., "PL", "US").</summary>
    public string? CountryCode { get; set; }

    /// <summary>Full formatted address as displayed by map provider.</summary>
    public string? FormattedAddress { get; set; }

    // ????????????????????????????????????????????????????????????
    //  Coordinates
    // ????????????????????????????????????????????????????????????

    /// <summary>Latitude coordinate (WGS84).</summary>
    public double? Latitude { get; set; }
    
    /// <summary>Longitude coordinate (WGS84).</summary>
    public double? Longitude { get; set; }

    // ????????????????????????????????????????????????????????????
    //  Map Provider Identifiers
    // ????????????????????????????????????????????????????????????

    /// <summary>Google Places API place ID.</summary>
    public string? GooglePlaceId { get; set; }
    
    /// <summary>OpenStreetMap node ID.</summary>
    public long? OsmNodeId { get; set; }
    
    /// <summary>OSM element type (node, way, relation).</summary>
    public string? OsmType { get; set; }
    
    /// <summary>Mapbox place ID.</summary>
    public string? MapboxPlaceId { get; set; }
    
    /// <summary>HERE Maps place ID.</summary>
    public string? HerePlaceId { get; set; }

    // ????????????????????????????????????????????????????????????
    //  Timezone & Virtual
    // ????????????????????????????????????????????????????????????

    /// <summary>IANA timezone identifier (e.g., "Europe/Warsaw").</summary>
    public string? TimeZone { get; set; }

    /// <summary>URL for virtual/online events.</summary>
    public string? VirtualUrl { get; set; }
    
    /// <summary>Whether this is a virtual (online) event.</summary>
    public bool IsVirtual { get; set; }

    // ????????????????????????????????????????????????????????????
    //  Additional Info
    // ????????????????????????????????????????????????????????????

    /// <summary>Contact phone number.</summary>
    public string? Phone { get; set; }
    
    /// <summary>Venue website URL.</summary>
    public string? Website { get; set; }

    /// <summary>Direct link to the location (Google Maps, event page, etc.).</summary>
    public string? Url { get; set; }
    
    /// <summary>Access instructions (e.g., "Ring bell #5", "Park in back").</summary>
    public string? AccessInstructions { get; set; }

    // ????????????????????????????????????????????????????????????
    //  Venue Details
    // ????????????????????????????????????????????????????????????

    /// <summary>Maximum venue capacity.</summary>
    public int? Capacity { get; set; }
    
    /// <summary>Whether parking is available.</summary>
    public bool HasParking { get; set; }
    
    /// <summary>Whether the venue is wheelchair accessible.</summary>
    public bool IsWheelchairAccessible { get; set; }
    
    /// <summary>Whether public transport is available nearby.</summary>
    public bool HasPublicTransport { get; set; }

    // ????????????????????????????????????????????????????????????
    //  Metadata
    // ????????????????????????????????????????????????????????????

    /// <summary>When the location was created.</summary>
    public DateTime CreatedAt { get; set; } = DateTime.UtcNow;
    
    /// <summary>When the location was last updated.</summary>
    public DateTime? UpdatedAt { get; set; }

    // ????????????????????????????????????????????????????????????
    //  Navigation
    // ????????????????????????????????????????????????????????????

    /// <summary>Events held at this location.</summary>
    public ICollection<Event> Events { get; set; } = new List<Event>();
}
