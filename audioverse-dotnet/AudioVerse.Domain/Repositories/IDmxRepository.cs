using AudioVerse.Domain.Entities.Dmx;

namespace AudioVerse.Domain.Repositories;

/// <summary>
/// Repository for DMX lighting scenes and sequences.
/// </summary>
public interface IDmxRepository
{
    // ????????????????????????????????????????????????????????????
    //  SCENES
    // ????????????????????????????????????????????????????????????

    /// <summary>
    /// Saves a new DMX scene.
    /// </summary>
    Task<int> SaveSceneAsync(DmxScene scene);

    /// <summary>
    /// Gets a scene by ID.
    /// </summary>
    Task<DmxScene?> GetSceneByIdAsync(int id);

    /// <summary>
    /// Gets all saved scenes.
    /// </summary>
    Task<IEnumerable<DmxScene>> GetAllScenesAsync();

    /// <summary>
    /// Updates an existing scene.
    /// </summary>
    Task<bool> UpdateSceneAsync(DmxScene scene);

    /// <summary>
    /// Deletes a scene.
    /// </summary>
    Task<bool> DeleteSceneAsync(int id);

    /// <summary>
    /// Gets a scene by name.
    /// </summary>
    Task<DmxScene?> GetSceneByNameAsync(string name);

    // ????????????????????????????????????????????????????????????
    //  SEQUENCES
    // ????????????????????????????????????????????????????????????

    /// <summary>
    /// Saves a new DMX sequence.
    /// </summary>
    Task<int> SaveSequenceAsync(DmxSceneSequence sequence);

    /// <summary>
    /// Gets a sequence by ID with its steps.
    /// </summary>
    Task<DmxSceneSequence?> GetSequenceByIdAsync(int id);

    /// <summary>
    /// Gets all sequences.
    /// </summary>
    Task<IEnumerable<DmxSceneSequence>> GetAllSequencesAsync();

    /// <summary>
    /// Updates an existing sequence.
    /// </summary>
    Task<bool> UpdateSequenceAsync(DmxSceneSequence sequence);

    /// <summary>
    /// Deletes a sequence.
    /// </summary>
    Task<bool> DeleteSequenceAsync(int id);

    // ????????????????????????????????????????????????????????????
    //  SEQUENCE STEPS
    // ????????????????????????????????????????????????????????????

    /// <summary>
    /// Adds a step to a sequence.
    /// </summary>
    Task<int> AddSequenceStepAsync(DmxSceneStep step);

    /// <summary>
    /// Gets steps for a sequence.
    /// </summary>
    Task<IEnumerable<DmxSceneStep>> GetSequenceStepsAsync(int sequenceId);

    /// <summary>
    /// Removes a step from a sequence.
    /// </summary>
    Task<bool> RemoveSequenceStepAsync(int stepId);

    /// <summary>
    /// Reorders steps in a sequence.
    /// </summary>
    Task<bool> ReorderSequenceStepsAsync(int sequenceId, IEnumerable<int> stepIdsInOrder);
}
