using AudioVerse.Domain.Entities.Events;

namespace AudioVerse.Domain.Repositories;

/// <summary>
/// Repository for event billing, expenses, and settlements.
/// </summary>
public interface IBillingRepository
{
    // ????????????????????????????????????????????????????????????
    //  EXPENSES
    // ????????????????????????????????????????????????????????????

    /// <summary>
    /// Adds an expense to an event.
    /// </summary>
    Task<int> AddExpenseAsync(EventExpense expense);

    /// <summary>
    /// Gets an expense by ID.
    /// </summary>
    Task<EventExpense?> GetExpenseByIdAsync(int id);

    /// <summary>
    /// Gets all expenses for an event.
    /// </summary>
    Task<IEnumerable<EventExpense>> GetExpensesByEventAsync(int eventId);

    /// <summary>
    /// Updates an expense.
    /// </summary>
    Task<bool> UpdateExpenseAsync(EventExpense expense);

    /// <summary>
    /// Deletes an expense.
    /// </summary>
    Task<bool> DeleteExpenseAsync(int id);

    /// <summary>
    /// Gets the total expenses for an event.
    /// </summary>
    Task<decimal> GetTotalExpensesAsync(int eventId);

    // ????????????????????????????????????????????????????????????
    //  EXPENSE SHARES
    // ????????????????????????????????????????????????????????????

    /// <summary>
    /// Adds a share assignment to an expense.
    /// </summary>
    Task<int> AddExpenseShareAsync(EventExpenseShare share);

    /// <summary>
    /// Gets shares for an expense.
    /// </summary>
    Task<IEnumerable<EventExpenseShare>> GetSharesByExpenseAsync(int expenseId);

    /// <summary>
    /// Deletes all shares for an expense.
    /// </summary>
    Task<bool> DeleteSharesByExpenseAsync(int expenseId);

    // ????????????????????????????????????????????????????????????
    //  PAYMENTS
    // ????????????????????????????????????????????????????????????

    /// <summary>
    /// Records a payment between participants.
    /// </summary>
    Task<int> AddPaymentAsync(EventPayment payment);

    /// <summary>
    /// Gets all payments for an event.
    /// </summary>
    Task<IEnumerable<EventPayment>> GetPaymentsByEventAsync(int eventId);

    /// <summary>
    /// Gets payments made by a specific user.
    /// </summary>
    Task<IEnumerable<EventPayment>> GetPaymentsByPayerAsync(int eventId, int payerId);

    /// <summary>
    /// Marks a payment as confirmed.
    /// </summary>
    Task<bool> ConfirmPaymentAsync(int paymentId);

    // ????????????????????????????????????????????????????????????
    //  SETTLEMENTS
    // ????????????????????????????????????????????????????????????

    /// <summary>
    /// Calculates balance for each participant in an event.
    /// Positive = owes money, Negative = is owed money.
    /// </summary>
    Task<IDictionary<int, decimal>> CalculateBalancesAsync(int eventId);

    /// <summary>
    /// Gets the amount a participant owes or is owed.
    /// </summary>
    Task<decimal> GetParticipantBalanceAsync(int eventId, int participantId);
}
