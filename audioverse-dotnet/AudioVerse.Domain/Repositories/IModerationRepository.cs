using AudioVerse.Domain.Entities.Admin;
using AudioVerse.Domain.Entities.Auth;

namespace AudioVerse.Domain.Repositories;

/// <summary>
/// Repository for content moderation including abuse reports, captchas, and honey tokens.
/// </summary>
public interface IModerationRepository
{
    // ????????????????????????????????????????????????????????????
    //  ABUSE REPORTS
    // ????????????????????????????????????????????????????????????

    /// <summary>
    /// Creates a new abuse report.
    /// </summary>
    /// <param name="report">The report to create</param>
    /// <returns>The ID of the created report</returns>
    Task<int> CreateReportAsync(AbuseReport report);

    /// <summary>
    /// Gets an abuse report by ID.
    /// </summary>
    Task<AbuseReport?> GetReportByIdAsync(int id);

    /// <summary>
    /// Gets pending (unresolved) abuse reports with pagination.
    /// </summary>
    Task<IEnumerable<AbuseReport>> GetPendingReportsAsync(int page = 1, int pageSize = 20);

    /// <summary>
    /// Gets all reports with optional filters.
    /// </summary>
    Task<IEnumerable<AbuseReport>> GetReportsAsync(
        bool? isResolved = null,
        string? contentType = null,
        int page = 1,
        int pageSize = 20);

    /// <summary>
    /// Resolves an abuse report.
    /// </summary>
    Task<bool> ResolveReportAsync(int id, int resolvedByUserId, string resolution);

    /// <summary>
    /// Gets the total count of reports matching the filter.
    /// </summary>
    Task<int> GetReportCountAsync(bool? isResolved = null, string? contentType = null);

    // ????????????????????????????????????????????????????????????
    //  CAPTCHAS
    // ????????????????????????????????????????????????????????????

    /// <summary>
    /// Creates a new captcha challenge.
    /// </summary>
    Task<int> CreateCaptchaAsync(Captcha captcha);

    /// <summary>
    /// Gets a captcha by token.
    /// </summary>
    Task<Captcha?> GetCaptchaAsync(string token);

    /// <summary>
    /// Marks a captcha as solved.
    /// </summary>
    Task<bool> MarkCaptchaSolvedAsync(string token);

    /// <summary>
    /// Deletes expired captchas.
    /// </summary>
    Task<int> DeleteExpiredCaptchasAsync();

    // ????????????????????????????????????????????????????????????
    //  HONEY TOKENS
    // ????????????????????????????????????????????????????????????

    /// <summary>
    /// Creates a new honey token.
    /// </summary>
    Task<int> CreateHoneyTokenAsync(HoneyToken token);

    /// <summary>
    /// Gets a honey token by its value.
    /// </summary>
    Task<HoneyToken?> GetHoneyTokenAsync(string token);

    /// <summary>
    /// Records an access to a honey token (security event).
    /// </summary>
    Task<bool> RecordHoneyTokenAccessAsync(int tokenId, string? ipAddress);

    /// <summary>
    /// Gets all honey tokens with their access counts.
    /// </summary>
    Task<IEnumerable<HoneyToken>> GetAllHoneyTokensAsync();
}
